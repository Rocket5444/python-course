# In python programing too, we must be able to execute instruction on a condition(s) being met.
# This is called conditional statements.
# This is an example of if else loop
a = int(input("Enter your age: "))

if(a>=18):
    print("You are above the age of consent")
    print("Good for you")

else:
    print("You are below the age of consent")
    print("It's not good for you stay away")

print("End of program")