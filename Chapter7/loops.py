# Let's assume that we have to print the number until the 5
print(1)
print(2)
print(3)
print(4)
print(5)

# This is the one way of doing it but it's not efficient.
# Same task can be done like  this with  help of loops:
for i in range(1, 6):
    print(i)

# Sometimes we want to repeat a set of statements in our program.
# Loops make it easy for a programmer to tell the computer which set of instructions to repeat and how!