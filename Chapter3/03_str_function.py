# Len function
# len() function is used to find the length of a string
name = "Kunal"

print(len(name)) 

#  Ends with function
#  endsWith() function is used to check if a string ends with a specified string or not

print(name.endswith("nal"))

# Start with function
# startswith() function is used to check if a string starts with a specified string or not

print(name.startswith("Kun"))

# Capitalize function
# capitalize() function is used to convert the first character of a string to uppercase

print(name.capitalize())

# Repalce function
# replace() function is used to replace a specified string with another string

print(name.replace("Kunal", "Kunal Kumar"))